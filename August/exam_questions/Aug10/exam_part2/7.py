# Ի՞նչ կտպվի հետևյալ կոդի կատարման արդյունքում (error-ի դեպքում հենց այդպես էլ նշել)։ Հիմնավորել պատասխանը։

a = [[]] * 3
a[1].append(1)
print(a) #[[1], [1], [1]] ,
